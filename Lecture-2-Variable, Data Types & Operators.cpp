    // "<<" / ">>" are overloaded operators
    // "cin" / "cout" are built in operators for io
    // "endl" is a built in operator for new line or use "\n"
    // Operators can be overloaded in C++
    // C++ has 4 types of operators
    // 1. Arithmetic (+ - * /)
    // 2. Relational (< > <= >= == !=)
    // 3. Bitwise (& | ^ << >>)
    // 4. Logical (&& || !)
    // 7. Unary (++ --) // pre/post update -> ++a/a++
    // 5. Ternary (? :)
    // 6. Assignment (= += -= *= /= %= <<= >>= &= |= ^= &&= ||=)

    cout << "ans " << ((2 == 3) || 2) << endl;
    return 0;
